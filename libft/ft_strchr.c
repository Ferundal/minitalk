#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	char	t_c;

	t_c = (char)c;
	while ((*s != t_c) && (*s != '\0'))
	{
		s++;
	}
	if (*s == t_c)
	{
		return ((char *)s);
	}
	return (NULL);
}
